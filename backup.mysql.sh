(( EUID == 0 )) && { echo >&2 "This script should not be run as root!"; exit 1; }

# -------------------------------------------------------------------------------------------------------------------- #
# CONFIGURATION.
# -------------------------------------------------------------------------------------------------------------------- #

mysqldump="$( command -v mysqldump )"
tar="$( command -v tar )"
date="$( command -v date )"
rm="$( command -v rm )"

# Help.
read -r -d '' help <<- EOF
Options:
  -u 'USER'                             MySQL user name.
  -p 'PASSWORD'                         MySQL user password.
  -d 'DB_1;DB_2;DB_3'                   MySQL databases.
EOF

# -------------------------------------------------------------------------------------------------------------------- #
# OPTIONS.
# -------------------------------------------------------------------------------------------------------------------- #

OPTIND=1

while getopts "u:p:d:h" opt; do
  case ${opt} in
    u)
      user="${OPTARG}"
      ;;
    p)
      password="${OPTARG}"
      ;;
    d)
      dbs="${OPTARG}"; IFS=';' read -ra dbs <<< "${dbs}"
      ;;
    h|*)
      echo "${help}"; exit 2
      ;;
  esac
done

shift $(( OPTIND - 1 ))

(( ! ${#dbs[@]} )) && exit 1

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

init() {
  # Functions.
  ts_date="$( _ts_date )"

  # Run.
  backup
}

# -------------------------------------------------------------------------------------------------------------------- #
# BACKUP: DATABASES.
# -------------------------------------------------------------------------------------------------------------------- #

backup() {
  for db in "${dbs[@]}"; do
    local name="${db}.${ts_date}.sql"

    echo "" && echo "--- OPEN: '${db}'"
    ${mysqldump} -u "${user}" -p"${password}" --single-transaction "${db}" > "${name}" \
      && ${tar} -cJf "${name}.tar.xz" "${name}" \
      && ${rm} -f "${name}"
    echo "" && echo "--- DONE: '${db}'" && echo ""
  done
}

# -------------------------------------------------------------------------------------------------------------------- #
# ------------------------------------------------< COMMON FUNCTIONS >------------------------------------------------ #
# -------------------------------------------------------------------------------------------------------------------- #

# Timestamp: Date.
_ts_date() {
  ${date} -u '+%Y-%m-%d.%H-%M-%S'
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< INIT FUNCTIONS >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

init "$@"; exit 0
